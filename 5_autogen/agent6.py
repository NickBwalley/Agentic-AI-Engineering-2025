from autogen_core import MessageContext, RoutedAgent, message_handler
from autogen_agentchat.agents import AssistantAgent
from autogen_agentchat.messages import TextMessage
from autogen_ext.models.openai import OpenAIChatCompletionClient
import messages
import random


class Agent(RoutedAgent):

    # Unique system message reflecting new characteristics and goals.

    system_message = """
    You are a savvy digital marketer. Your task is to develop innovative marketing strategies leveraging Agentic AI, or enhance existing campaigns.
    Your personal interests lie in these sectors: E-commerce, Tourism.
    You are intrigued by campaigns that incorporate user engagement and data analytics.
    You prefer approaches focused on authentic brand storytelling over mere advertisement.
    You have a proactive mindset, thrive on creative challenges and innovation. You tend to think outside the box, but can sometimes overlook details.
    Your weaknesses: you might be overly critical of conventional methods, and sometimes chase too many trends simultaneously.
    You should convey your marketing insights in a compelling and persuasive manner.
    """

    CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER = 0.4

    def __init__(self, name) -> None:
        super().__init__(name)
        model_client = OpenAIChatCompletionClient(model="gpt-4o-mini", temperature=0.7)
        self._delegate = AssistantAgent(name, model_client=model_client, system_message=self.system_message)

    @message_handler
    async def handle_message(self, message: messages.Message, ctx: MessageContext) -> messages.Message:
        print(f"{self.id.type}: Received message")
        text_message = TextMessage(content=message.content, source="user")
        response = await self._delegate.on_messages([text_message], ctx.cancellation_token)
        idea = response.chat_message.content
        if random.random() < self.CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER:
            recipient = messages.find_recipient()
            message = f"Here is my marketing strategy. It may not align perfectly with your expertise, but please refine it and enhance it. {idea}"
            response = await self.send_message(messages.Message(content=message), recipient)
            idea = response.content
        return messages.Message(content=idea)